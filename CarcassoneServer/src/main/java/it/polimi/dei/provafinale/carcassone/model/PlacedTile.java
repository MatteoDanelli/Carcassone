package it.polimi.dei.provafinale.carcassone.model;

/**
 * This class model the tile placed in the map.
 * It's only an helper class.
 * @author Canidio Andrea
 *
 */
public class PlacedTile {

	private Position position;
	private DisplayedTile tile;

	/**
	 * Create a placedTile with the parameter passed.
	 * @param positon position where place the tile.
	 * @param tile tile to place.
	 */
	public PlacedTile(Position positon, DisplayedTile tile) {
		this.position = positon;
		this.tile = tile;
	}

	/**
	 * Getter of the placedTile.
	 * @return tile placed.
	 */
	public DisplayedTile getTile() {
		return tile;
	}

	/**
	 * Getter of the position of the placedTile.
	 * @return position of placed tile.
	 */
	public Position getPosition() {
		return position;
	}
}
